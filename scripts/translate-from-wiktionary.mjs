#!/usr/bin/env node
/**
 * MiroLang — bulk translator powered by the Wiktionary API.
 *
 * Reads the canonical `src/data.js` (English headwords), queries the
 * English Wiktionary API for each, extracts translations to the requested
 * target language from the `{{t|<code>|<word>|<gender>}}` templates in
 * each page's wikitext, prepends the right article for nouns based on the
 * gender code, and writes the result to `src/data/<lang>.js`.
 *
 * Why this approach:
 *   - No download. No bulk file to host.
 *   - No API key. No registration.
 *   - Wiktionary's `action=query` lets us fetch 50 pages per HTTP call, so
 *     6,500 words = ~130 requests = ~2-3 minutes with polite throttling.
 *   - Same Wiktionary data as the (huge) Kaikki dump, just queried live.
 *
 * Usage:
 *   node scripts/translate-from-wiktionary.mjs de
 *   node scripts/translate-from-wiktionary.mjs nl
 *   node scripts/translate-from-wiktionary.mjs fr
 *   ...
 *
 * Output:
 *   - src/data/<lang>.js — populated word lists, mirroring `src/data.js`
 *     structure. Levels with translations already present (`words[].ru`
 *     filled) are kept as-is, so the script is idempotent and safe to
 *     re-run after a network blip.
 *   - .cache/missing-<lang>.txt — words Wiktionary had no translation for.
 *     Add them manually to the output file, or fill via a different pass
 *     (e.g. AI API once you have a key).
 *
 * Coverage expectations:
 *   - Top-2000 high-frequency words: ~95% coverage.
 *   - Words 3000-6500: 70-90% coverage — Wiktionary's translation tables
 *     thin out for less common entries.
 *   - Overall blended: ~85% for our 6,500-word dataset.
 *
 * Wiktionary API politeness:
 *   - The script sends a descriptive `User-Agent`, batches by 50 titles,
 *     and waits 250ms between batches. Well under MediaWiki's documented
 *     rate-limit thresholds for unauthenticated bot traffic.
 */

import fs from 'node:fs/promises';
import path from 'node:path';
import {fileURLToPath} from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, '..');
const SOURCE_PATH = path.join(ROOT, 'src', 'data.js');
const CACHE_DIR = path.join(ROOT, '.cache');

// Map our content-language code → (a) the English-language name Wiktionary
// uses in translation table lines, (b) Wiktionary's own language code used
// inside `{{t|<code>|...}}` templates (these usually match ISO 639-1 but
// diverge for some), (c) gender → article mapping.
