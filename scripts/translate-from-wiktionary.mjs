const TARGET_LANG = process.argv[2];
if (!TARGET_LANG || !LANG_CONFIG[TARGET_LANG]) {
  console.error('Usage: node scripts/translate-from-wiktionary.mjs <lang>');
  console.error('Supported:', Object.keys(LANG_CONFIG).join(', '));
  process.exit(1);
}

const cfg = LANG_CONFIG[TARGET_LANG];
const TARGET_PATH = path.join(ROOT, 'src', 'data', `${TARGET_LANG}.js`);
const MISSING_PATH = path.join(CACHE_DIR, `missing-${TARGET_LANG}.txt`);
const BATCH_SIZE = 50; // MediaWiki API max for prop=revisions
const THROTTLE_MS = 250;

const USER_AGENT =
  'MiroLang/2.3 vocabulary translation script (https://mirolang.ru; one-time fill)';

// ---------- File I/O ----------

async function loadJsModule(filePath) {
  const text = await fs.readFile(filePath, 'utf8');
  const marker = 'export default';
  const idx = text.indexOf(marker);
  if (idx === -1) throw new Error(`'export default' not found in ${filePath}`);
  const body = text.slice(idx + marker.length).replace(/;\s*$/, '');
  return new Function(`return ${body}`)();
}

async function writeTargetModule(filePath, data) {
  const header = `/**
 * MiroLang data — ${cfg.name} (${TARGET_LANG}) → EN pair.
 *
 * Generated by scripts/translate-from-wiktionary.mjs (Wiktionary API).
 * Mirrors the module/level structure of \`src/data.js\`.
 *
 * Source: English Wiktionary translation tables (CC BY-SA 4.0).
 * Re-run the script to refresh translations or fill new languages.
 *
 * TECH DEBT: translations live in a field called \`ru\` (rather than
 * \`${TARGET_LANG}\` or \`translation\`) so existing screens that read
 * \`word.ru\` keep working without a sweeping refactor.
 */

`;
  const body = JSON.stringify(data, null, 2);
  await fs.writeFile(filePath, header + 'export default ' + body + ';\n');
}

// ---------- Wiktionary API ----------

/**
 * Fetch wikitext for up to BATCH_SIZE titles in one HTTP call.
 * Returns Map<normalized-lowercased-title, wikitext|null>.
 */
async function fetchWikitextBatch(titles) {
  const params = new URLSearchParams({
    action: 'query',
    prop: 'revisions',
    rvprop: 'content',
    rvslots: 'main',
    format: 'json',
    formatversion: '2',
    redirects: '1',
    titles: titles.join('|'),
  });
  const url = `https://en.wiktionary.org/w/api.php?${params}`;

  const resp = await fetch(url, {headers: {'User-Agent': USER_AGENT}});
  if (!resp.ok) throw new Error(`Wiktionary API ${resp.status}: ${url}`);
  const data = await resp.json();

  // formatversion 2 gives us a flat `pages` array. `normalized` and
  // `redirects` tell us how the requested title was resolved — we map back
  // to the original requested key so the caller can look it up.
  const out = new Map();
  const titleResolution = new Map(); // resolved → original (lowercased)
  for (const t of titles) titleResolution.set(t.toLowerCase(), t.toLowerCase());

  const normalized = data.query?.normalized || [];
  for (const n of normalized) {
    titleResolution.set(n.to.toLowerCase(), n.from.toLowerCase());
  }
  const redirects = data.query?.redirects || [];
  for (const r of redirects) {
    const from = r.from.toLowerCase();
    const to = r.to.toLowerCase();
    // chain: to → from (so anything that points at `to` resolves to original `from`)
    const root = titleResolution.get(from) || from;
    titleResolution.set(to, root);
  }

  for (const page of data.query?.pages || []) {
    const original = titleResolution.get(page.title.toLowerCase()) || page.title.toLowerCase();
    if (page.missing) {
      out.set(original, null);
      continue;
    }
    const wikitext = page.revisions?.[0]?.slots?.main?.content || '';
    out.set(original, wikitext);
  }
  // Anything we asked about that wasn't in `pages` (rare) → mark missing.
  for (const t of titles) {
    if (!out.has(t.toLowerCase())) out.set(t.toLowerCase(), null);
  }
  return out;
}

/**
 * Detect that a page has its translations moved to a subpage. Wiktionary
 * does this for heavyweight entries (e.g. "teacher", "money") to keep the
 * main page parseable. The relevant templates are
 *   {{see translation subpage|<POS>}}
 *   {{translation only|<POS>}}
 * and the canonical subpage lives at `<title>/translations`.
 */
function hasSubpageMarker(wikitext) {
  if (!wikitext) return false;
  return /\{\{(?:see translation subpage|translation only)\b/i.test(wikitext);
}

/**
 * Pick the best translation from wikitext for our target language.
 *
 * Wiktionary translation tables look like:
 *   {{trans-top|sense gloss}}
 *   * German: {{t|de|Haus|n}}
 *   * French: {{t+|fr|maison|f}}
 *   {{trans-bottom}}
 *
 * We pull the first `{{t...|<code>|<word>(|<gender>)?...}}` that matches
 * our target language code, ignoring later sense groups. This is usually
 * the primary meaning — good enough for a flashcard.
 */
function extractTranslation(wikitext) {
  if (!wikitext) return null;
  // Wiktionary uses several template variants for translation entries:
  //   {{t|<code>|word|gender}}        — basic, light pages
  //   {{t+|<code>|word|gender}}       — marks target-Wiktionary page exists
  //   {{tt|<code>|word|gender}}       — lazy-loaded variant used on heavy
  //   {{tt+|<code>|word|gender}}        translation tables (door, war, music
  //   {{t-|...}} / {{tt-|...}}          — common words tend to land here)
  //
  // The pattern below matches all variants in one shot.
  const re = new RegExp(
    String.raw`\{\{tt?[\+\-]?\|` +
      cfg.code +
      String.raw`\|([^|}]+)((?:\|[^|}]*)*)\}\}`,
    'g',
  );
  let match;
  while ((match = re.exec(wikitext)) !== null) {
    const word = match[1].trim();
    if (!word) continue;
    // Trailing pipes hold gender (m/f/n/c/p), transliteration (tr=), etc.
    const tail = match[2] || '';
    let gender = null;
    for (const part of tail.split('|')) {
      const p = part.trim();
      if (!p) continue;
      if (/^[mfncp]$/.test(p) && cfg.article[p]) {
        gender = p;
        break;
      }
    }
    return {word, gender};
  }
  return null;
}

function formatTranslation(record) {
  if (!record) return '';
  let w = record.word.trim();
  // Strip parenthetical / qualifier suffixes Wiktionary occasionally embeds.
  w = w.replace(/\s*\([^)]*\)\s*$/, '').trim();
  // Some entries pack multiple synonyms — take the first.
  w = w.split(/[,/;]/)[0].trim();
  if (!w) return '';
  if (record.gender && cfg.article[record.gender]) {
    return `${cfg.article[record.gender]} ${w}`;
  }
  return w;
}

// ---------- Diff / merge ----------

function levelNeedsWork(sourceLevel, targetLevel) {
  if (!targetLevel) return true;
  if (!Array.isArray(targetLevel.words)) return true;
  if (targetLevel.words.length !== sourceLevel.words.length) return true;
  return targetLevel.words.some(
    w => !w || typeof w.ru !== 'string' || w.ru.length === 0,
  );
}

function findLevel(target, moduleId, levelId) {
  const mod = target.find(m => m.id === moduleId);
  if (!mod) return null;
  return mod.data.find(l => l.id === levelId) || null;
}

// ---------- Main ----------

const sleep = ms => new Promise(r => setTimeout(r, ms));

async function main() {
  await fs.mkdir(CACHE_DIR, {recursive: true});

  const source = await loadJsModule(SOURCE_PATH);

  let target;
  try {
    target = await loadJsModule(TARGET_PATH);
    console.log(`Loaded existing ${TARGET_PATH} — will fill missing levels only.`);
  } catch (e) {
    if (e.code === 'ENOENT') {
      console.log(`No existing ${TARGET_LANG}.js — starting from source skeleton.`);
      target = JSON.parse(JSON.stringify(source));
      for (const m of target) for (const l of m.data) l.words = [];
    } else {
      throw e;
    }
  }

  // Collect every word we still need.
  const needed = new Set();
  for (const mod of source) {
    for (const level of mod.data) {
      const tgtLevel = findLevel(target, mod.id, level.id);
      if (!levelNeedsWork(level, tgtLevel)) continue;
      for (const w of level.words) needed.add(w.word);
    }
  }

  if (needed.size === 0) {
    console.log('All levels already complete — nothing to do.');
    return;
  }

  console.log(`Need to translate ${needed.size} unique words.`);
  console.log(`Fetching from Wiktionary in batches of ${BATCH_SIZE}...`);

  // Wiktionary is case-sensitive. Some single-letter pronouns ("I") live on
  // capitalized pages; everything else is lowercased there. We try the
  // lowercased title first (which is what our source uses) and let
  // MediaWiki's `redirects=1` follow capitalization redirects.
  // Helper: drive one pass that fetches `titles` in batches, then dispatches
  // each result through `handle(originalWord, wikitext)`. Used for all three
  // passes below.
  async function runPass(titles, originalWords, label, handle) {
    if (titles.length === 0) return;
    if (label) console.log(`\n${label} (${titles.length})`);
    for (let i = 0; i < titles.length; i += BATCH_SIZE) {
      const titleBatch = titles.slice(i, i + BATCH_SIZE);
      const wordBatch = originalWords.slice(i, i + BATCH_SIZE);
      process.stdout.write(`  ${i + titleBatch.length}/${titles.length}...`);
      let pages;
      try {
        pages = await fetchWikitextBatch(titleBatch);
      } catch (e) {
        console.error(`\n  batch failed: ${e.message}`);
        await sleep(2000);
        pages = await fetchWikitextBatch(titleBatch);
      }
      for (let j = 0; j < titleBatch.length; j++) {
        handle(wordBatch[j], pages.get(titleBatch[j].toLowerCase()));
      }
      process.stdout.write(` ok\n`);
      if (i + BATCH_SIZE < titles.length) await sleep(THROTTLE_MS);
    }
  }

  const wordsList = [...needed];
  const translations = new Map(); // lowercased word -> {word, gender}|null
  const subpageRetry = []; // words with `{{see translation subpage|...}}`
  const capitalizeRetry = []; // words whose English entry lives on `Word`

  // Pass 1 — original case (lowercase, as in source data).
  await runPass(wordsList, wordsList, 'Pass 1: main pages', (w, wikitext) => {
    const tr = extractTranslation(wikitext);
    if (tr) {
      translations.set(w.toLowerCase(), tr);
      return;
    }
    if (hasSubpageMarker(wikitext)) {
      subpageRetry.push(w);
      return;
    }
    // Wiktionary is case-sensitive. Some entries (nationalities, names,
    // titles like "British", "French", "Mrs") live on a capitalized page,
    // and the lowercase page only carries other-language sections.
    if (w.length > 0 && w[0] !== w[0].toUpperCase()) {
      capitalizeRetry.push(w);
      return;
    }
    translations.set(w.toLowerCase(), null);
  });

  // Pass 2 — `<word>/translations` subpages for heavyweight entries.
  await runPass(
    subpageRetry.map(w => `${w}/translations`),
    subpageRetry,
    'Pass 2: subpages',
    (w, wikitext) => {
      translations.set(w.toLowerCase(), extractTranslation(wikitext));
    },
  );

  // Pass 3 — capitalized retry for proper-noun-style entries.
  await runPass(
    capitalizeRetry.map(w => w[0].toUpperCase() + w.slice(1)),
    capitalizeRetry,
    'Pass 3: capitalized retry',
    (w, wikitext) => {
      let tr = extractTranslation(wikitext);
      if (!tr && hasSubpageMarker(wikitext)) {
        // Capitalized entry with subpage — give it one more shot.
        // (Rare in practice; we don't bother batching this.)
        translations.set(w.toLowerCase(), null);
        return;
      }
      translations.set(w.toLowerCase(), tr);
    },
  );

  // Fill missing levels.
  const missingWords = [];
  let filled = 0;

  for (const sourceModule of source) {
    for (const sourceLevel of sourceModule.data) {
      const tgtLevel = findLevel(target, sourceModule.id, sourceLevel.id);
      if (!levelNeedsWork(sourceLevel, tgtLevel)) continue;

      const translatedWords = sourceLevel.words.map(w => {
        const record = translations.get(w.word.toLowerCase());
        const translated = formatTranslation(record);
        if (!translated) {
          missingWords.push(`${w.word} (M${sourceModule.id}/L${sourceLevel.id}/id${w.id})`);
        } else {
          filled++;
        }
        return {
          id: w.id,
          word: w.word,
          ru: translated,
          transcription: w.transcription,
        };
      });

      let lvl = findLevel(target, sourceModule.id, sourceLevel.id);
      if (!lvl) {
        let mod = target.find(m => m.id === sourceModule.id);
        if (!mod) {
          mod = {id: sourceModule.id, title: sourceModule.title, data: []};
          target.push(mod);
        }
        lvl = {
          id: sourceLevel.id,
          title: sourceLevel.title,
          description: sourceLevel.description,
          words: translatedWords,
        };
        mod.data.push(lvl);
      } else {
        lvl.words = translatedWords;
        if (!lvl.title) lvl.title = sourceLevel.title;
        if (!lvl.description) lvl.description = sourceLevel.description;
      }
    }
  }

  await writeTargetModule(TARGET_PATH, target);
  await fs.writeFile(MISSING_PATH, missingWords.join('\n') + '\n');

  const total = filled + missingWords.length;
  const pct = total ? ((filled / total) * 100).toFixed(1) : '0.0';

  console.log(`
Done.
  Output:    ${TARGET_PATH}
  Filled:    ${filled} / ${total} (${pct}%)
  Missing:   ${missingWords.length} words — see ${MISSING_PATH}
  Manual:    review the missing list and spot-check a few translated rows
             before shipping. Wiktionary quality drops below the top 2,000.
`);
}

main().catch(err => {
  console.error('\nFatal:', err.message || err);
  process.exit(1);
});
